class Dog {
    String name;
    int age;

    public static void main(String[] args) {
        // Create Dog object
        Dog myDog = new Dog();

        // Set attributes
        myDog.name = "Buddy";
        myDog.age = 5;

        // Print output
        System.out.println("Dog's Name: " + myDog.name + ", Age: " + myDog.age);
    }
}
